%1.Sistema de Distribuição-Conjunto de instalações e equipamentos elétricos encarregados de distribuir a energia elétrica aos grandes, 
%médios e pequenos consumidores.
%2.Redes e linhas de distribuição-Conjunto de estruturas, utilidades, condutores 
%e equipamentos elétricos, aéreos ou subterrâneos, utilizados para a distribuição da energia elétrica, 
%operando em baixa, média e/ou alta tensão de distribuição.
%3.Subestação de distribuição (SED)-Subestação que conecta o Sistema de Distribuição de Alta Tensão 
%–SDAT ao Sistema de Distribuição de Média Tensão –SDMT, contendo transformadores de força.
%4.Sistema de distribuição de média tensão (SDMT)-Conjunto de linhas de 
%distribuição e de equipamentos associados em tensões típicas superiores a 1 kV e inferiores a 69 kV, na maioria das vezes com função primordial de atendimento a unidades consumidoras, podendo conter geração distribuída.
%5.Redes de Distribuição Secundárias (BT)-Parte do sistema elétrico de 
%distribuição que deriva dos transformadores ligados às redes primárias (MT)
%e se destina ao suprimento dos consumidores atendidos em tensão secundária e da iluminação pública.
%6.Sistema de distribuição de baixa tensão (SDBT)-Conjunto de redes de 
%distribuição e de equipamentos associados em tensões nominais inferiores ou iguais a 1 kV.
%7.Alimentador Primário-Parte de uma rede de distribuição primária que alimenta, 
%diretamente ou por intermédio de seus ramais, os primários dos transformadores 
%de distribuição da concessionária e/ou consumidores. Constitui-se de tronco e ramais.
%8.Tronco de Alimentador Primário-Parte principal de um alimentador primário,
%que deriva diretamente da subestação até o primeiro equipamento de proteção, 
%se caracteriza por maior seção de condutores, atende maior parcela ou ao total
%da carga do alimentador, além de interligações com troncos de alimentadores vizinhos, conforme a configuração da rede.
%9.Ramal de Alimentador Primário-Parte de um alimentador primário que deriva
%do tronco, e na maioria das vezes, caracteriza-se por condutores de seções 
%inferiores. Atende a parcelas de carga, conforme a sua distribuição em relação ao tronco.