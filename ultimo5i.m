%?Quando uma corrente elétrica alternada flui por um condutor, a densidade 
%de corrente elétrica será distribuída de forma não-uniformeao longo da secção transversal, 
%sendo que a maior densidade de corrente elétrica é localizada na superfície do condutor.
%?O efeito pelicular têm como características o aumento na resistência elétrica 
%efetiva e a redução na indutância interna.
%?Tais propriedades elétricas são funções da frequênciana qual a corrente 
